
<h3 class="text-dark">Recursos Humanos</h3>
<img src="imagem/recursos humanos.jpg" alt="Minha Foto" style="width: 100px; float: left; margin-right: 20px; box-shadow: 1px 1px 1px #000">
<p style="text-indent: 40px; text-align: justify; line-height: 30px;">
    O Recursos Humanos é um setor da empresa responsável por diversos processos que envolvem a companhia e seus colaboradores. Como o nome já diz, a área é responsável por cuidar das pessoas que fazem parte da organização. 

O departamento também é conhecido como RH e surgiu no século XX, visando resolver as burocracias que fazem parte da vida de um funcionário na empresa. Como nome já diz, o RH trata dos recursos humanos da empresa ou seja, de pessoas.

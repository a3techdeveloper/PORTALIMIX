
<h3 class="text-dark">Atendente de Farmácia</h3>
<img src="imagem/atendente de farmácia.jpg" alt="Minha Foto" style="width: 100px; float: left; margin-right: 20px; box-shadow: 1px 1px 1px #000">
<p style="text-indent: 40px; text-align: justify; line-height: 30px;">
    Um(a) atendente de farmácia (também chamado de balconista ou auxiliar de farmácia) não tem as mesmas responsabilidades de um(a) farmacêutico(a).
    Assim como os farmacêuticos, os atendentes ocupam um dos cargos mais importante desse estabelecimento. O atendente de farmácia é responsável por quase toda a rotina diária de atendimento aos clientes e manutenção da farmácia. Por isso, encontrar profissionais com alguma formação para essa função é fundamental.
</p>

<p style="text-indent: 40px; text-align: justify; line-height: 30px;">
    Stencyl é uma ferramenta de desenvolvimento de videogames que permite aos usuários criar videogames 2D para computadores, dispositivos móveis e a web. O software está disponível gratuitamente, com opções de publicação selecionadas disponíveis para compra.

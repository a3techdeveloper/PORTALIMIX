
<h3 class="text-dark">Home Office</h3>
<img src="imagem/home-office.jpg" alt="Minha Foto" style="width: 100px; float: left; margin-right: 20px; box-shadow: 1px 1px 1px #000">
<p style="text-indent: 40px; text-align: justify; line-height: 30px;">
    Home office ou escritório em casa, também chamado de trabalho remoto, trabalho à distância ou teletrabalho, é uma tendência mundial que a cada ano ganha mais adeptos. Basicamente, trata-se de permitir que o colaborador trabalhe de qualquer lugar.[1] Desde o inicio da pandemia provocada pela COVID-19 se tornou uma tendência mundial para tentar diminuir os impactos financeiros causado. O home office trata-se de permitir que uma pessoa tenha a possibilidade de trabalhar de qualquer lugar, desde que tenha a disposição de algumas ferramentas para seu trabalho, como computador com conexão à internet. 
</p>


<h3 class="text-dark">Logística e Transporte</h3>
<img src="imagem/logistica_.jpg" alt="Minha Foto" style="width: 100px; float: left; margin-right: 20px; box-shadow: 1px 1px 1px #000">
<p style="text-indent: 40px; text-align: justify; line-height: 30px;">
    A logística é a área administrativa que cuida do planejamento, execução e distribuição de produtos, principalmente no que diz respeito ao transporte e armazenamento.

O objetivo é garantir que o produto chegue até o cliente da forma mais rápida, eficiente e econômica possível, desde o ponto de origem até o ponto de consumo. 
</p>

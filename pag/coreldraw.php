
<h3 class="text-dark">CorelDraw</h3>
<img src="imagem/coreldraw.png" alt="Minha Foto" style="width: 100px; float: left; margin-right: 20px; box-shadow: 1px 1px 1px #000">
<p style="text-indent: 40px; text-align: justify; line-height: 30px;">
    O CorelDRAW é um completo programa de desenhos que trabalha com
imagens vetoriais, oferecendo várias ferramentas e efeitos que permitem uma arte de alta qualidade e com acabamento profissional. Estas
imagens criadas a partir do CorelDRAW são formadas por um conjunto
de equações matemáticas instruindo o computador como desenhar as
linhas, e que cores elas terão.
Considerado o software de computação gráfica mais popular para PC’s,
o CorelDRAW 10 é sem dúvida um estúdio artístico completo. Apesar do
pacote compreender outros programas, vamos voltar toda nossa aten-
ção ao programa central - suficiente para criarmos ilustrações precisas,
desenhar trabalhos artísticos de forma livre, desenhos técnicos envolvidos em projetos de engenharia, enfim, qualquer coisa que sua imagina-
ção permitir.
</p>

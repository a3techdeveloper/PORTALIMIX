
<h3 class="text-dark">Redes Sociais</h3>
<img src="imagem/midias_sociais.jpg" alt="Minha Foto" style="width: 100px; float: left; margin-right: 20px; box-shadow: 1px 1px 1px #000">
<p style="text-indent: 40px; text-align: justify; line-height: 30px;">
    Mídias Sociais são sites e aplicativos que permitem conexão e interação entre os usuários. Também conhecidas como redes sociais, as mídias sociais mais populares atualmente são Facebook, Youtube, Instagram e WhatsApp.
    As mídias sociais ou redes sociais tratam-se de tecnologias e plataformas que permitem a criação e o compartilhamento de informação e conteúdos em formatos passíveis de transmissão por canais digitais.
</p>
